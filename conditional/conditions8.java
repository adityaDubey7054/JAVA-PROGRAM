import java.util.*;

//write a program to print absolute value of a number entered by user
public class conditions8 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        if (a > 0) {
            System.out.println(a);

        } else {
            System.out.println(-a);
        }

    }
}
